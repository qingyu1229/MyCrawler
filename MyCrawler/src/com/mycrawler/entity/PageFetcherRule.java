package com.mycrawler.entity;

public class PageFetcherRule {
	private Long id;
	private String website_id;
	private String contentName;
	private String contentReg;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getWebsite_id() {
		return website_id;
	}
	public void setWebsite_id(String website_id) {
		this.website_id = website_id;
	}
	public String getContentName() {
		return contentName;
	}
	public void setContentName(String contentName) {
		this.contentName = contentName;
	}
	public String getContentReg() {
		return contentReg;
	}
	public void setContentReg(String contentReg) {
		this.contentReg = contentReg;
	}
	
	
}
